"""Generate docs/SOP/MODULE_TEST_COVERAGE_V1.md from git-tracked src/ + tests/."""

from __future__ import annotations

import argparse
import subprocess
import sys
from pathlib import Path


def _repo_root() -> Path:
    return Path(__file__).resolve().parents[1]


def _git_ls_files(repo: Path, pattern: str) -> list[str]:
    out = subprocess.check_output(
        ["git", "ls-files", pattern],
        cwd=repo,
        text=True,
    )
    return [line.strip() for line in out.splitlines() if line.strip()]


def _module_stems(paths: list[str], prefix: str) -> list[str]:
    stems: list[str] = []
    for path in paths:
        if not path.startswith(prefix) or not path.endswith(".py"):
            continue
        name = Path(path).stem
        if name == "__init__":
            continue
        stems.append(name)
    return sorted(set(stems))


def _test_index(test_paths: list[str]) -> dict[str, list[str]]:
    index: dict[str, list[str]] = {}
    for path in test_paths:
        if not path.startswith("tests/test_") or not path.endswith(".py"):
            continue
        stem = Path(path).name.removeprefix("test_").removesuffix(".py")
        index.setdefault(stem, []).append(path)
    return index


def _render_table(title: str, modules: list[str], test_index: dict[str, list[str]]) -> list[str]:
    lines = [f"### {title}", "", "| Module | Test file(s) |", "|--------|--------------|"]
    for mod in modules:
        hits = test_index.get(mod, [])
        if hits:
            cell = ", ".join(f"`{h}`" for h in hits)
        else:
            partial = [
                tp
                for key, tps in test_index.items()
                if mod in key or key in mod
                for tp in tps
            ]
            cell = ", ".join(f"`{p}`" for p in sorted(set(partial))) if partial else "— (integration only)"
        lines.append(f"| `{mod}` | {cell} |")
    lines.append("")
    return lines


def generate(repo: Path) -> str:
    src_py = _git_ls_files(repo, "src")
    test_py = _git_ls_files(repo, "tests")

    engine = _module_stems([p for p in src_py if p.startswith("src/engine/")], "src/engine/")
    data = _module_stems([p for p in src_py if p.startswith("src/data/")], "src/data/")
    viz = _module_stems([p for p in src_py if p.startswith("src/viz/")], "src/viz/")
    test_index = _test_index(test_py)

    body: list[str] = [
        "# Module test coverage map v1",
        "",
        "**Plane:** CONTROL-PLANE · **Generated by:** `scripts/generate_module_test_coverage_map.py`",
        "",
        "Quick reference: which `src/` modules have a matching `tests/test_<module>.py`.",
        "A dash means no direct file — the module may still be covered by integration tests (`test_mvp1_*`, etc.).",
        "",
        "Regenerate:",
        "",
        "```bash",
        "python scripts/generate_module_test_coverage_map.py --write",
        "```",
        "",
    ]
    body.extend(_render_table("Engine (`src/engine/`)", engine, test_index))
    body.extend(_render_table("Data (`src/data/`)", data, test_index))
    body.extend(_render_table("Viz (`src/viz/`)", viz, test_index))

    direct = sum(1 for m in engine + data + viz if test_index.get(m))
    total = len(engine) + len(data) + len(viz)
    body.extend(
        [
            "## Summary",
            "",
            f"- **Direct test file match:** {direct}/{total} modules",
            f"- **Engine:** {sum(1 for m in engine if test_index.get(m))}/{len(engine)}",
            f"- **Data:** {sum(1 for m in data if test_index.get(m))}/{len(data)}",
            f"- **Viz:** {sum(1 for m in viz if test_index.get(m))}/{len(viz)}",
            "",
        ]
    )
    return "\n".join(body)


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--write",
        action="store_true",
        help="Write docs/SOP/MODULE_TEST_COVERAGE_V1.md",
    )
    args = parser.parse_args(argv)
    repo = _repo_root()
    text = generate(repo)
    if args.write:
        out = repo / "docs" / "SOP" / "MODULE_TEST_COVERAGE_V1.md"
        out.write_text(text, encoding="utf-8")
        print(f"wrote {out}")
    else:
        print(text)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
